
import java.util.*;

/**
 * 
 */
public class Notification {

    /**
     * Default constructor
     */
    public Notification() {
    }

    public Notification(int numNot, String message, java.util.Date date) {
        NumNot = numNot;
        Message = message;
        Date = date;
    }

    /**
     * 
     */
    public int NumNot;

    /**
     * 
     */
    public String Message;

    /**
     * 
     */
    public Date Date;

}